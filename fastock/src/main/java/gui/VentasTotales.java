    void MostrarDatos(){
        ArrayList<venta> ventas = new ArrayList<venta>();
        serviciodb serv = new serviciodb();
        ventas=serv.getVentaTotal();
         modell.Usuario Usuario;    
        DefaultTableModel modelo=new DefaultTableModel();
            modelo.addColumn("NÂ° venta");
            modelo.addColumn("Monto total");
            modelo.addColumn("Fecha");
            modelo.addColumn("id del usuario");
            VentasTable.setModel(modelo);
        String []datos=new String[6];
        Iterator<venta> ite=ventas.iterator();
        while(ite.hasNext()){
        venta venta1= ite.next();
            datos[0]=venta1.getIdventa().toString();
            datos[1]=venta1.getMonto().toString();
            datos[2]=venta1.getFecha().toString();           
            Usuario=serv.getBuscarUsuario(venta1.getUsuario_idusuario());
            datos[3]= Usuario.getUsuario();
        
            modelo.addRow(datos);
                            }
        VentasTable.setModel(modelo);
    }
